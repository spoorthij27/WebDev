    <h1 style="color:#059B14;padding-left: 3%;padding-top: 1%;">Know more about Lawns</h1>
    <p>
      <pre style="font-size:18px;color:white;padding-left: 3%;padding-top: 1%;">
 A lawn is an area where grass is grown as a green carpet for a landscape and is the basic feature of any garden. It serves to enhance the beauty of the garden, be it larger or smaller. Proper
 lawn maintenance plays a crucial part in any landscape design. A beautiful well maintained lawn can make the entire landscape look good, whereas a lawn that is not maintained can completely
 ruin it's beauty. The lawn not only harmonizes with a decor of the drawing room, but also sets of a suitable background for a specimen tree or a shrub, as well as for colourful beds and
 borders. The position of the lawn largely depends upon the layout of the garden in relation to the house.  In general lawn should be wide open with access to direct sunshine, especially
 in front of a rockery and a water pool.

 Site and Soil
 After choosing the site, the next important factor for consideration is the size and shape of the lawn. The preparation of site includes digging, leveling and enriching the soil with organic
 manures or by amending with fertile soil. If the soil is very heavy, coarse sand may be added by removing subsoil to a depth of 20 cm. The ideal soil pH should be 5.0 to 5.6.  If it is very
 acidic 500 g/m2 lime should be added and to clayey loam or alkaline soil gypsum of the same quantity may be added. Provision of drainage for excess rain water should be made if the ground
 is not sloppy.

 Levelling
 The site should be thoroughly levelled with spade, pebbles and weeds are hand picked.  The soil is rolled with a roller.  Weeds especially nut grass should not be allowed to grow and should be
 removed with roots for at least 2 to 3 times.

 Methods of lawn making

 1. Seeding
 The most popular grass suitable for seeding is "Doob" grass (Cynodon dactylon).  It has the fast spreading mat forming habit, radially forms roots at the nodes, the foliage is dark green,
 narrow with parallel vines. A lawn from seed is thought of only when grass roots are not available.  About 30 kg of seed is required for planting one hectare.  The soil should be reduced
 to fine tilth and given a light rolling. The site should be divided into suitable small squares or rectangles, the seeds are mixed with double the quan­tity of finely sieved soil and should
 be rolled again and wa­tered liberally with rose can. The seeds take four to five weeks for germination. Care should be taken not to flood the site.  For the first few times, the grasses are
 cut with a scythe. Lawn mower may be used for easy maintenance and for its spreading.

 2. Turfing
 The turfs are nothing but pieces of earth with compact grasses on them. These turfs should be cut uniformly in squares from a place where the grass is short, compact and free from weeds.
 These turfs should be placed on the prepared ground site, side by side and beaten down flat with a turf beater. The cavities in between should be filled with fine soil. The entire turfed
 area should be rolled and watered liberally. This is the most expensive way of lawn making.

 3. Turf plastering
 The doob grass can be procured in large quantities free from weeds and chopped properly into small bits of 5-7 cm long. Two baskets of chopped grass pieces should be mixed well with
 one basket each of garden soil and fresh cow dung and a shovel full of wood ash with required quantity of water to form a thick pasty substance. This mixture is then spread uniformly
 on the surface of a previously wetted perfectly leveled ground to a thickness of at least 2.5cm and watering should be done with a rose can. The next day, ground should be rolled and
 the grass should be allowed to spread. The grass will shoot up in a fortnight. To start with, cut with a scythe and after three months, use the lawn mower. 

 4. Dibbling roots
 This is the cheapest but time consuming method. Small pieces of grass roots should be dibbled 10 – 15 cm apart in a leveled ground when it is wet after rain. The roots spread and grow
 underground in the course of six months making a fairly compact lawn by frequent mowing, rolling and watering.
