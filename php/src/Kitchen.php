      <h1 style="color:#059B14;padding-left: 3%;padding-top: 1%;">Know more about kitchen gardening</h1>
      <p><pre style="font-size: 18px;color:white;padding-left: 3%;padding-top: 1%;"> 
 There are two types of kitchen gardening. The first type takes place in your kitchen and can involve either re-growing veggies from food scraps or growing herbs and veggies on your
 windowsill.
 The other type is to grow outdoors maybe in the backyard or the rooftops. <br>
 Advantages of Kitchen garden :

 1.Supply fresh fruits and vegetables high in nutritive value.
 2.Supply fruits and vegetables free from toxic chemicals.
 3.Help to save expenditure on purchase of vegetables.
 4.Vegetables harvested from home garden taste better than those purchased from market.
 5.Effective utilization of kitchen waste water and kitchen waste materials.
 6.Exercise to the body and mind.
 <br>
 The main thing to think about when planning your plot is what it is you actually have to work with. It helps if you’ve been able to study it over the year, so that you know where the first
 of the sun’s rays fall, where the frost lingers and which plants might be affected by strong winds. It’s really interesting to see how your plot changes with each season, and keeping note
 of all its quirks and characteristics will help you sow and plant with the confidence that you’ve given your crop the very best chance to thrive. If you are discovering your plot for the
 first time, general rules of thumb will apply – and in fact are more applicable to a small space, as there is less possibility of fluctuating temperatures and idiosyncratic light levels –
 so finding out which direction your garden or balcony faces is a good start.
 <br>
 Early spring is an ideal time to start vegetable gardening, but you can of course, start planning well ahead. Decide on the size of the plot you would like. Make sure you keep to a size you
 can manage. A large vegetable garden with room to grow everything will take a lot of work, both preparation and maintenance. A smaller plot with dwarf varieties, or produce mixed in among
 flower beds, or planting in containers would work better if you don't have much time for gardening.

 The amount of space and light levels of your chosen growing spot will be the determining factor in what you grow, and you need to understand how much room you can offer each plant to
 grow in a raised bed or pot, before you get started. 'Measure the site to know square footage,' advises McKenzie, and 'Plan the layout of the garden to ensure that all the plants are
 accessible and will receive enough sunlight.'

 You need to treat plants a little like royalty, as if they feel cramped, then you can’t guarantee that they will produce as well as they may have done! To understand how much space each
 vegetable or fruit plant will need, check the seed packet and then select your pots and containers accordingly because this will make your life a heck of a lot easier. 

 To achieve the best results when growing fruit and vegetables you need:

 An open, sunny spot:  Preferably one that gets the morning sun, and around six to eight hours of direct sunlight daily. To grow quickly and well, vegetables need as much light as possible,
 so track the sun throughout the day to see where shadows fall. If you don’t have these conditions, there are some crops that tolerate shade, such as cherries, blackberries, raspberries,
 rhubarb and blackcurrants. 'Figure out what parts of the area receive the most sunlight to locate the most sun-loving plants there or to cascade the plants from lowest to highest to 
 prevent the second from shading the first'.

 Wind protection: A permeable barrier, such as a picket fence, hedge or windbreak can filter its effect.

 Fertile soil: Soil enriched with compost. Learning more on growing organic is a wise move here.

